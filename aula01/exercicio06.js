// Declare uma variável booleana que informa se está chovendo 
// e utilize-a em uma estrutura condicional (if) para exibir 
// uma mensagem informando se é preciso levar um guarda-chuva ou 
// não dependendo do valor da variável.
const estaChovendo = false;
if(estaChovendo){
    console.log("Leve um guarda-chuva")
}else{
    console.log("Não é necessário levar um guarda-chuva")
}