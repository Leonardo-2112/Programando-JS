//Crie uma variável com uma string e utilize métodos de manipulação de strings, 
//como toUpperCase, toLowerCase, slice ou outros, para modificar a string original. 
//Exiba os resultados finais no console.
let texto = "Manipulando Strings";

let textoMaiusculo = texto.toUpperCase();
let textoMinusculo = texto.toLowerCase();
let quantidadeCaracteres = texto.length;

console.log(`Texto em Maiúsculo -> ${textoMaiusculo}`);
console.log(`Texto em Minúsculo -> ${textoMinusculo}`);
console.log(`Quantidade de Caracteres -> ${quantidadeCaracteres}`);

